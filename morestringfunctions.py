message = "Hello world"

# Gives the positions of the first letter of the string from where it starts
print(message.find('world'))

# Counts the number of letters in the string
print(message.count('o'))

# Capitaises the string variable
print(message.capitalize())

# Replaces the first string with the second string
print(message.replace('Hello','Hi'))




